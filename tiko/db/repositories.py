    def _require_account(self, session: Session, account_id: str) -> AccountRecord:
        """Load an account or fail when persisted run state is inconsistent.

        Args:
            session: Active SQLAlchemy session.
            account_id: Account identifier.

        Returns:
            Persisted account record.

        Raises:
            LookupError: If the account row is missing.
        """

        record = session.get(AccountRecord, account_id)
        if record is None:
            raise LookupError(f"Missing account row {account_id}.")
        return record

