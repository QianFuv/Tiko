    def _merge_run(self, session: Session, run: SimulationRun) -> None:
        """Merge a run and its account into the active session.

        Args:
            session: Active SQLAlchemy session.
            run: Simulation run to merge.
        """

        self._merge_account(session, run.account)
        session.merge(
            SimulationRunRecord(
                run_id=str(run.run_id),
                name=run.name,
                status=run.status,
                mode=run.mode,
                account_id=str(run.account.account_id),
                symbols=run.symbols,
                start_sim_time=run.start_sim_time,
                current_sim_time=run.current_sim_time,
                end_sim_time=run.end_sim_time,
                speed_multiplier=run.speed_multiplier,
                config=run.config,
                created_at=run.created_at,
            )
        )

