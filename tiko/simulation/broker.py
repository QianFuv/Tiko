"""Internal simulated broker and matching behavior."""

from decimal import Decimal

from tiko.domain.order import Fill, OrderRequest, SimOrder
from tiko.simulation.fee import FeeEngine
from tiko.simulation.matching import MatchingEngine
from tiko.simulation.slippage import SlippageEngine


class SimBroker:
    """Convert internal order requests into immediate simulated fills."""

    def __init__(
        self,
        fee_bps: Decimal = Decimal("5"),
        slippage_bps: Decimal = Decimal("2"),
        maker_fee_bps: Decimal | None = None,
        matching_engine: MatchingEngine | None = None,
    ) -> None:
        """Initialize broker fee and slippage parameters.

        Args:
            fee_bps: Simulated taker fee in basis points.
            slippage_bps: Simulated slippage in basis points.
            maker_fee_bps: Optional simulated maker fee in basis points.
            matching_engine: Optional matching engine override.
        """

        self.fee_bps = fee_bps
        self.maker_fee_bps = maker_fee_bps if maker_fee_bps is not None else fee_bps
        self.slippage_bps = slippage_bps
        self._matching_engine = matching_engine or MatchingEngine(
            fee_engine=FeeEngine(fee_bps),
            maker_fee_engine=FeeEngine(self.maker_fee_bps),
            slippage_engine=SlippageEngine(slippage_bps),
        )

    def submit_market_order(
        self, order_request: OrderRequest, reference_price: Decimal
    ) -> tuple[SimOrder, Fill]:
        """Fill an internal market order request at a simulated price.

        Args:
            order_request: Internal order request generated by the portfolio layer.
            reference_price: Current market reference price.

        Returns:
            Simulated order and fill records.
        """

        return self._matching_engine.match_market_order(order_request, reference_price)

    def submit_limit_order(
        self, order_request: OrderRequest, reference_price: Decimal
    ) -> tuple[SimOrder, Fill | None]:
        """Submit an internal limit order request to simulated matching.

        Args:
            order_request: Internal limit order request generated by the
                portfolio layer.
            reference_price: Current market reference price.

        Returns:
            Simulated order and optional fill.
        """

        return self._matching_engine.match_limit_order(order_request, reference_price)
