    def _optional_aware_datetime(self, value: datetime | None) -> datetime | None:
        """Return an aware timestamp when a value is present.

        Args:
            value: Optional timestamp value.

        Returns:
            Timezone-aware timestamp or `None`.
        """

        if value is None:
            return None
        return self._aware_datetime(value)

    def _aware_datetime(self, value: datetime) -> datetime:
        """Return a timezone-aware timestamp.

        Args:
            value: Timestamp loaded from the database.

        Returns:
            Timestamp with UTC timezone when the database returned a naive value.
        """

        if value.tzinfo is None:
            return value.replace(tzinfo=UTC)
        return value

    def _read_realtime_envelope_string(
        self,
        envelope: dict[str, object],
        key: str,
    ) -> str:
        """Read a required realtime envelope string value.

        Args:
            envelope: Realtime event envelope.
            key: Required key.

        Returns:
            Non-empty string value.

        Raises:
            ValueError: If the value is missing or empty.
        """

        value = envelope.get(key)
        if value is None:
            raise ValueError(f"Realtime envelope field {key} is required.")
        text = str(value)
        if not text:
            raise ValueError(f"Realtime envelope field {key} must not be empty.")
        return text

    def _read_realtime_envelope_time(
        self,
        envelope: dict[str, object],
    ) -> datetime:
        """Read the required realtime envelope simulated time.

        Args:
            envelope: Realtime event envelope.

        Returns:
            Simulated event timestamp.

        Raises:
            ValueError: If the timestamp is missing or invalid.
        """

        value = envelope.get("simulated_time")
        if isinstance(value, datetime):
            return value
        if isinstance(value, str):
            return datetime.fromisoformat(value.replace("Z", "+00:00"))
        raise ValueError("Realtime envelope field simulated_time is required.")

    def _read_realtime_envelope_payload(
        self,
        envelope: dict[str, object],
    ) -> dict[str, object]:
        """Read the required realtime envelope payload.

        Args:
            envelope: Realtime event envelope.

        Returns:
            Realtime event payload.

        Raises:
            ValueError: If the payload is missing or not an object.
        """

        value = envelope.get("payload")
        if not isinstance(value, dict):
            raise ValueError("Realtime envelope field payload must be an object.")
        return value
