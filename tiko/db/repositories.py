class SimulationRepository:
    """Persist and load simulation artifacts through SQLAlchemy sessions."""

    def __init__(self, session_factory: sessionmaker[Session]) -> None:
        """Initialize the repository.

        Args:
            session_factory: SQLAlchemy session factory.
        """

        self._session_factory = session_factory

    def save_run(self, run: SimulationRun) -> None:
        """Persist a simulation run and its nested account state.

        Args:
            run: Simulation run to persist.
        """

        with self._session_factory() as session:
            self._merge_run(session, run)
            session.commit()

    def save_step_result(
        self,
        result: SimulationStepResult,
        realtime_envelopes: Sequence[dict[str, object]] = (),
    ) -> None:
        """Persist all durable artifacts generated by one simulation step.

        Args:
            result: Simulation step output to persist.
            realtime_envelopes: Canonical realtime envelopes produced by the step.
        """

        with self._session_factory() as session:
            self._merge_run(session, result.run)
            self._add_candle(session, result.run.run_id, result.candle)
            self._add_orderbook_snapshot(
                session, result.run.run_id, result.orderbook_snapshot
            )
            self._merge_feature_snapshot(session, result.feature_snapshot)
            self._merge_market_event(session, result.run.run_id, result.event)
            self._merge_observation_snapshot(session, result.observation)
            self._merge_decision(session, result.decision)
            self._merge_agent_run(session, result.agent_run)
            for message in result.agent_messages:
                self._merge_agent_message(session, message)
            self._merge_risk_review(session, result.run.run_id, result.risk_review)
            if result.order is not None:
                self._merge_order(session, result.order)
            if result.fill is not None:
                self._merge_fill(session, result.fill)
            self._replace_positions(session, result.run.run_id, result.positions)
            if result.ledger_entry is not None:
                self._merge_ledger_entry(session, result.ledger_entry)
            if result.funding_ledger_entry is not None:
                self._merge_ledger_entry(session, result.funding_ledger_entry)
            self._merge_portfolio_snapshot(session, result.portfolio_snapshot)
            self._merge_metric_snapshot(session, result.metric_snapshot)
            for envelope in realtime_envelopes:
                self._merge_realtime_event(session, envelope)
            session.commit()

    def save_agent_trace(
        self,
        observation: Observation,
        decision: TradeIntent,
        agent_run: AgentRun,
        agent_messages: Sequence[AgentMessage],
    ) -> None:
        """Persist a worker-generated agent trace.

        Args:
            observation: Source observation snapshot.
            decision: Validated trade intent.
            agent_run: Agent run trace artifact.
            agent_messages: Agent messages for the trace.
        """

        with self._session_factory() as session:
            self._merge_observation_snapshot(session, observation)
            self._merge_decision(session, decision)
            self._merge_agent_run(session, agent_run)
            for message in agent_messages:
                self._merge_agent_message(session, message)
            session.commit()

    def save_decision(self, decision: TradeIntent) -> None:
        """Persist one structured trade intent.

        Args:
            decision: Trade intent to persist.
        """

        with self._session_factory() as session:
            self._merge_decision(session, decision)
            session.commit()

    def get_run(self, run_id: UUID) -> SimulationRun | None:
        """Load one simulation run by ID.

        Args:
            run_id: Simulation run identifier.

        Returns:
            Simulation run or `None`.
        """

        with self._session_factory() as session:
            run_record = session.get(SimulationRunRecord, str(run_id))
            if run_record is None:
                return None
            account_record = self._require_account(session, run_record.account_id)
            return self._to_run(run_record, account_record)

    def list_runs(self) -> list[SimulationRun]:
        """List persisted simulation runs ordered by creation time.

        Returns:
            Persisted simulation runs.
        """

        with self._session_factory() as session:
            records = session.scalars(
                select(SimulationRunRecord).order_by(SimulationRunRecord.created_at)
            ).all()
            return [
                self._to_run(record, self._require_account(session, record.account_id))
                for record in records
            ]

